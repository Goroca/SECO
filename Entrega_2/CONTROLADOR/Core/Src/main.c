void startSystem(void) {
	encoder = 0;
	last_encoder = 0;
	pos = START_POSITION;
	diff = 0;
	extra = 0;
	count = 0;
	setReference(LAPS);
	HAL_GPIO_WritePin(GPIOC, GPIO_PIN_1, GPIO_PIN_SET); //ACTIVA ENABLE
	HAL_TIM_PWM_Start(&htim2, TIM_CHANNEL_1);
	HAL_TIM_PWM_Start(&htim2, TIM_CHANNEL_2);
	HAL_TIM_Encoder_Start(&htim1, TIM_CHANNEL_ALL);
	direction= POSITIVE;

	Kp =  0.5;

	HAL_TIM_Base_Start_IT(&htim3);

}

void setVoltage(float voltage) {
//	VOLTAGE = MAX_VOLTAGE*(PERIOD/MAX_PERIOD) FORMULA

	uint32_t period;
	//PERIOD VALUES BETWEEN 0 AND 4199
	direction = POSITIVE;
	if(voltage<0){
		voltage= voltage*-1;
		direction = NEGATIVE;
	}
	period = (uint32_t) (ENCODER_TOTAL_POS *voltage) / MAX_VOLTAGE;
	if (voltage)
		period = period - 1;

	if (direction == POSITIVE) {
		__HAL_TIM_SET_COMPARE(&htim2, TIM_CHANNEL_1, period);
		__HAL_TIM_SET_COMPARE(&htim2, TIM_CHANNEL_2, 0);
	} else {
		__HAL_TIM_SET_COMPARE(&htim2, TIM_CHANNEL_1, 0);
		__HAL_TIM_SET_COMPARE(&htim2, TIM_CHANNEL_2, period);
	}
}

void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim) {
	if (htim == &htim3) { //SALTA CADA MILISEGUNDO
		controller();
		if(count == FINISH_TIME)
			HAL_TIM_Base_Stop_IT(&htim3);
		count++;
	}

}
void controller(void) {
	readEncoder();
	int32_t diff2 = reference - pos;
	uint8_t aux[10];
	int32_t represent = pos - START_POSITION;
	sprintf(aux, "%9d", (int) represent);
	aux[9] = '\n';

 	HAL_UART_Transmit(&huart2, aux, 10, HAL_MAX_DELAY);

	float voltage = diff2*Kp ;

	setVoltage(voltage);
}

void setReference(uint32_t vueltas) {
	reference = START_POSITION + vueltas * REDUCTER;
}


void readEncoder(void) {
	encoder = htim1.Instance->CNT;
	diff = encoder - last_encoder;//+ extra;
	int32_t diff_abs;
	diff_abs = diff;
	if(diff<0)
		diff_abs = diff*-1;
	if(diff_abs < ENCODER_THRESHOLE){
		pos += diff;
	}
	last_encoder = encoder;
}



